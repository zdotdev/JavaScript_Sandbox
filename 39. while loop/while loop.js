// while loop use boolean expression to run a loop while specific status is not achieved.
function whileLoop(){
    let i = 0;
    while(i <= 3){
        console.log(i);
        i++;
    };
};
whileLoop();

// do while loop This loop will execute the code block once, before checking if the condition is true, then it will repeat the loop as long as the condition is true.
function doWhile(){
    let i = 0;
    do{
        console.log(i);
        i++
    }
    while(i <= 3)
}
doWhile();